    /**
     * Code Redundancy to eliminate repetitive use
     *
     * Check Credentials
     *
     * Need username Need password
     *
     */
    private void checkCredentials() {
        Object[] userData = new Object[6];
        boolean test = false;
        User myUser = new User();

        String userNameValue = jTextField1.getText();
        char[] passwordTemp = jPasswordField1.getPassword();

        String passwordValue = "";
        for (char x : passwordTemp) {
            passwordValue += x;
        }

        test = myUser.authenticateUser(userNameValue, passwordValue);
        userData = myUser.getUser(userNameValue);

        if (test) {
            JFrame f = new MainMenu(userData);

            f.setVisible(true);
            //new MainMenu(userData).setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Username/Password!!!",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }

